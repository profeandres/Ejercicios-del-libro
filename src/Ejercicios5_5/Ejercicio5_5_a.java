package Ejercicios5_5;

/*
Construir un programa que muestre la longitud de las palabras “casa”,
“amigo” y “universitario”
*/
public class Ejercicio5_5_a {
    public static void main(String[] args) {
        String casa="casa", amigo="amigo",uni="universitario";


        System.out.println("La palabra "+casa+" tiene "+casa.length()+ " carácteres");
        System.out.println("La palabra "+amigo+" tiene "+amigo.length()+ " carácteres");
        System.out.println("La palabra "+uni+" tiene "+uni.length()+ " carácteres");
    }
}
